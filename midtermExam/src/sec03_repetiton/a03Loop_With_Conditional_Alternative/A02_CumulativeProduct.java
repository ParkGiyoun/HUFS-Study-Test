package sec03_repetiton.a03Loop_With_Conditional_Alternative;

public class A02_CumulativeProduct {

	public static void main(String[] args) {
		int max = Math.max(1, 2);
		System.out.println(max);
		
		int min = Math.min(1, 2);
		System.out.println(min);
		
		Double sqrt = Math.sqrt(9.0);//제곱근
		System.out.println(sqrt);
		
		double pow = Math.pow(3, 4); //앞에거 ^ 뒤에거
		System.out.println(pow);
		
		int abs = Math.abs(-3); //absolute value (절댓값)
		System.out.println(abs);
		
		double round = Math.round(3.6); //가장 가까운 정수
		System.out.println(round);
		
		double floor = Math.floor(3.8); //같거나 작은 정수 중에 가장 가까운 숫자
		System.out.println(floor);
		
		double ceil = Math.ceil(3.2); // 같거나 큰 정수 중에 가장 가까운 숫자
		System.out.println(ceil);
	}
}